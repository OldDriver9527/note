package org.olddriver.syntax;
/**
 * 
 * java 基本语法笔记
 *
 */
public class LearnJavaSyntax {
	/*
	 * 关键字  Keyword
	 * 程序中被赋予特殊含义的单词
	 * 
	 * 标识符 Identifier
	 * 程序中自定义的名称，包名，类名，方法名，变量名，常量名都属于标识符
	 * 标识符命名规则
	 * 1.标识符由数字，字母，_，$ 等字符构成
	 * 2.数字不能作为标识符开头，标识符不能与关键字重复
	 * 标识符命名规范
	 * 1.包名要求所有字母小写
	 * 2.类名要求首字母大写，其后每个单词首字母大写
	 * 3.方法名要求首字母小写，其后每个单词首字母大写
	 * 4.变量名要求首字母小写，其后每个单词首字母大写
	 * 5.常量名要求所有字母大写
	 * 
	 * 注释 Comment
	 * 注解说明的文字，java中包含单行注释，多行注释，文档注释
	 * 单行注释中可以嵌套单行注释，多行注释
	 * 多行注释中不能嵌套多行注释
	 * 文档注释java特有，定义在类，成员方法，成员变量上
	 * 文档注释能够从源文件中提取生成说明文档，javadoc命令默认提取public，protected成员文档注释
	 * 文档注释中可以包括html，文档标签
	 * 常用文档标签
	 * @see	引用其他类或类中成员
	 * 				使用格式：
	 * 				@see fully-qualified-classname
	 * 				@see fully-qualified-classname#method-name
	 * @link	行内引用其他类
	 * 				使用格式：
	 * 				{@link package.class#member label}
	 * 				label 超链接名称
	 * 
	 */
	

	
}
